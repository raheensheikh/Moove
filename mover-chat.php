<script>
document.addEventListener('DOMContentLoaded', function () {
    // Get references to the chat box, toggle button, and close button
    var chatBox = document.querySelector('.chat_comming');
    var toggleButton = document.querySelector('.toggle-chat');
    var closeButton = document.querySelector('.close-chat');

    // Function to toggle the chat box visibility
    function toggleChatBox() {
        chatBox.classList.toggle('visible');
    }

    // Function to close the chat box
    function closeChatBox() {
        chatBox.classList.remove('visible');
    }

    // Add click event listener to the toggle button
    toggleButton.addEventListener('click', toggleChatBox);

    // Add click event listener to the close button
    closeButton.addEventListener('click', closeChatBox);
});

</script>

